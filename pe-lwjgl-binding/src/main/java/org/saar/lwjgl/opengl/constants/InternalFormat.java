package org.saar.lwjgl.opengl.constants;

import org.lwjgl.opengl.GL11;
import org.lwjgl.opengl.GL14;
import org.lwjgl.opengl.GL30;

public enum InternalFormat {

    // 1 Component
    R8(GL30.GL_R8),
    R8I(GL30.GL_R8I),
    R8UI(GL30.GL_R8UI),

    R16(GL30.GL_R16),
    R16F(GL30.GL_R16F),
    R16I(GL30.GL_R16I),
    R16UI(GL30.GL_R16UI),

    R32F(GL30.GL_R32F),
    R32I(GL30.GL_R32I),
    R32UI(GL30.GL_R32UI),

    // 2 Components,
    RG8(GL30.GL_RG8),
    RG8I(GL30.GL_RG8I),
    RG8UI(GL30.GL_RG8UI),

    RG16(GL30.GL_RG16),
    RG16F(GL30.GL_RG16F),
    RG16I(GL30.GL_RG16I),
    RG16UI(GL30.GL_RG16UI),

    RG32F(GL30.GL_RG32F),
    RG32I(GL30.GL_RG32I),
    RG32UI(GL30.GL_RG8UI),

    // 3 Components
    R3_G3_B2(GL11.GL_R3_G3_B2),
    R11F_G11F_B10F(GL30.GL_R11F_G11F_B10F),

    RGB4(GL11.GL_RGB4),
    RGB5(GL11.GL_RGB5),
    RGB10(GL11.GL_RGB10),
    RGB12(GL11.GL_RGB12),

    RGB8(GL11.GL_RGB8),
    RGB8I(GL30.GL_RGB8I),
    RGB8UI(GL30.GL_RGB8UI),

    RGB16(GL11.GL_RGB16),
    RGB16F(GL30.GL_RGB16F),
    RGB16I(GL30.GL_RGB16I),
    RGB16UI(GL30.GL_RGB16UI),

    RGB32F(GL30.GL_RGB32F),
    RGB32I(GL30.GL_RGB32I),
    RGB32UI(GL30.GL_RGB32UI),

    // 4 Components
    RGB5_A1(GL11.GL_RGB5_A1),
    RGB10_A2(GL11.GL_RGB10_A2),

    RGBA2(GL11.GL_RGBA2),
    RGBA4(GL11.GL_RGBA4),
    RGBA12(GL11.GL_RGBA12),

    RGBA8(GL11.GL_RGBA8),
    RGBA8I(GL30.GL_RGBA8I),
    RGBA8UI(GL30.GL_RGBA8UI),

    RGBA16(GL11.GL_RGBA16),
    RGBA16F(GL30.GL_RGBA16F),
    RGBA16I(GL30.GL_RGBA16I),
    RGBA16UI(GL30.GL_RGBA16UI),

    RGBA32F(GL30.GL_RGBA32F),
    RGBA32I(GL30.GL_RGBA32I),
    RGBA32UI(GL30.GL_RGBA32UI),

    // Depth
    DEPTH16(GL14.GL_DEPTH_COMPONENT16),
    DEPTH24(GL14.GL_DEPTH_COMPONENT24),
    DEPTH32(GL14.GL_DEPTH_COMPONENT32),

    // Stencil
    STENCIL_INDEX8(GL30.GL_STENCIL_INDEX8),

    // Depth stencil
    DEPTH24_STENCIL8(GL30.GL_DEPTH24_STENCIL8),
    DEPTH32F_STENCIL8(GL30.GL_DEPTH32F_STENCIL8),
    ;

    private final int value;

    InternalFormat(int value) {
        this.value = value;
    }

    public int get() {
        return this.value;
    }
}
